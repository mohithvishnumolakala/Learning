
public class DoWhileloop
{
    public static void DoWhileLoop()
    {
        int i = 1;
        do
        {
            Console.WriteLine(i);
            i++;
        }
        while (i <= 10);

    }
    public static void DoWhile()
    {
        int i = 10;
        do
        {
            Console.WriteLine(i);
            i--;
        }
        while (i > 0);

    }
}
